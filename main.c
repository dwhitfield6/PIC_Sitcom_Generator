void main(void)
{
    unsigned int PotValue = 0;
    ConfigureOscillator();
    InitApp();
    Init_System();

    TurnOnAmp();
    delayUS(1000000);
    OrangeLEDON();
    UnMuteAmp();

    while(1)
    {
        PotValue = ReadPot();
        Volume = Scale(PotValue,0,0x0FFF,0,8);
        Nop();
    }
}
